/**
 * 비AI 상담 응답 생성기
 *
 * 기존에는 Dify(외부 유료 LLM)에 의존해 상담 응답을 받았으나,
 * 토큰 비용으로 서비스가 중단되는 문제가 있어 외부 의존성을 제거하고
 * 페르소나(who) · 말투(how) · 고민(worry)을 조합하는 규칙 기반 생성기로 대체했다.
 *
 * - 외부 호출/비용 0, 즉시 응답
 * - 위기 신호(자해·자살 등) 감지 시 페르소나 응답 대신 안전 안내를 반환
 * - rng 주입으로 결정론적 테스트 가능
 */

export interface CounselInput {
  who: string;
  how: string;
  worry: string;
}

export type Rng = () => number;

const pick = <T>(arr: readonly T[], rng: Rng): T =>
  arr[Math.floor(rng() * arr.length) % arr.length];

/* ──────────────────────────── 안전 가드 ──────────────────────────── */

const CRISIS_PATTERNS: RegExp[] = [
  /자살/,
  /죽고\s*싶/,
  /죽고싶/,
  /자해/,
  /살기\s*싫/,
  /살\s*의미/,
  /살고\s*싶지\s*않/,
  /사라지고\s*싶/,
  /목숨/,
  /뛰어내리/,
];

export const isCrisis = (text: string): boolean =>
  CRISIS_PATTERNS.some((re) => re.test(text ?? ""));

