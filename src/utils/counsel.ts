/* ──────────────────────────── 생성 ──────────────────────────── */

export function generateCounselingResponse(
  input: CounselInput,
  rng: Rng = Math.random
): string {
  const { who, how, worry } = input;

  if (isCrisis(worry)) return crisisResponse();

  const voice = PERSONA[who] ?? PERSONA_FALLBACK(who);
  const tone = TONE[how] ?? "";
  const opener = pick(OPENERS, rng);
  const nudge = pick(NUDGES, rng);

  const body = [opener, tone].filter(Boolean).join(" ");

  return [voice.call, "", body, "", nudge, "", voice.line]
    .join("\n")
    .replace(/\n{3,}/g, "\n\n")
    .trim();
}

export default generateCounselingResponse;
